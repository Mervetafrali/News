package com.yazlab.NewsDTO;

public class newsDTO {

	private int kkk;
	private int likes;
	private int dislikes;
	private int views;

	public int getKkk() {
		return kkk;
	}

	public void setKkk(int kkk) {
		this.kkk = kkk;
	}

	public int getLikes() {
		return likes;
	}

	public void setLikes(int likes) {
		this.likes = likes;
	}

	public int getDislikes() {
		return dislikes;
	}

	public void setDislikes(int dislikes) {
		this.dislikes = dislikes;
	}

	public int getViews() {
		return views;
	}

	public void setViews(int views) {
		this.views = views;
	}
	
}
